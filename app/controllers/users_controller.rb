  def show
  end

  # GET /users/new
  def new
    @user = User.new
  end

  # GET /users/1/edit
  def edit
    user = find_user_by_id params[:user_id]
    params[:address] = encrypt(params[:address]) unless params[:address].blank?
    user.update user_params if user
  end

